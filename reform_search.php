              <div class="text_block">
                <p>為使事實真相及時確定，減少當事人因事實不清而纏訟，或因法院見解分歧而無所適從，本院已著手修改相關組織法及訴訟法，將人力、資源儘量集中於地方法院，使地方法院有充分資源可以充分、及時調查證據，讓事實真相及早確定。</p>
                <h4>(一)訴訟程序改革</h4>
                <p>本院業已成立民事、刑事及行政訴訟法研究修正委員會，將就與金字塔訴訟制度有關的民事、刑事及行政訴訟法進行修正，以及落實堅實第一審的相關配套措施。例如民事訴訟採行審理計畫制度、引進專家參與訴訟、擴大律師強制代理等相關議題；刑事訴訟強化偵查中辯護功能，擴大緩起訴範圍；第一審增訂審查程序、精緻化準備程序、擴大強制辯護範圍、擴大簡式審判範圍、強化量刑協商制度等；行政訴訟採行部分許可上訴制、擴大簡易訴訟程序範圍、建立行政法院調解機制、擴大強制代理的範圍之功能等。</p>
                <h4>(二)法院組織配套變革</h4>
                <p>配合各種金字塔型訴訟程序修法進度，修正相關組織法、法官法。其中終審法院法官之選任，重在選任過程之多元民主性，因此應將選任重心，置於多元組成的遴選委員會，由司法院院長提名應任名額2倍之人數後，由遴選委員會選出應任人選，由司法院報請總統任命之。</p>
